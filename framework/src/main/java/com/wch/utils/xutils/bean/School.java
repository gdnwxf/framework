package com.wch.utils.xutils.bean;

public class School {
	private int id ;
	private String  scName ;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the scName
	 */
	public String getScName() {
		return scName;
	}
	/**
	 * @param scName the scName to set
	 */
	public void setScName(String scName) {
		this.scName = scName;
	}
}
